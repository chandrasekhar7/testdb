CREATE PROCEDURE [dbo].[UpdateCallRPC] (
	 @CallSid CHAR(34)
	,@RPC BIT
)
AS
	BEGIN
	SET NOCOUNT ON
/****************************************************************************************************
--------------Change History----------------------section START--------------------------------------
*****************************************************************************************************
	DEVELOPER NAME		DATE				DESC

*****************************************************************************************************
--------------Change History----------------------section END----------------------------------------
*****************************************************************************************************/

	UPDATE Calls
		SET RPC = @RPC
		WHERE CallSid = @CallSid
END
SET ANSI_NULLS ON
