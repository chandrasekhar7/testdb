CREATE PROCEDURE [dbo].[UpdateCallListItem] (
	 @ItemID INT
	,@StatusID INT
	,@SipCode INT = NULL
)
AS
	BEGIN
	SET NOCOUNT ON
/****************************************************************************************************
--------------Change History----------------------section START--------------------------------------
*****************************************************************************************************
	DEVELOPER NAME		DATE				DESC

*****************************************************************************************************
--------------Change History----------------------section END----------------------------------------
*****************************************************************************************************/

		UPDATE CallListItems
			SET CallStatusID = @StatusID
			,StatusDate = GETDATE()
			,SipResponse = @SipCode
		WHERE @ItemID = ItemID


END


SET QUOTED_IDENTIFIER ON
