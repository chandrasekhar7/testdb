CREATE PROCEDURE [dbo].[GetDirectory] 
AS
	BEGIN
	SET NOCOUNT ON
/****************************************************************************************************
--------------Change History----------------------section START--------------------------------------
*****************************************************************************************************
	DEVELOPER NAME		DATE				DESC

*****************************************************************************************************
--------------Change History----------------------section END----------------------------------------
*****************************************************************************************************/

	SELECT
		 DirID
		,Dir
	FROM Directories
	WHERE DirID = (SELECT MAX(DirID) FROM Directories)
		
END


