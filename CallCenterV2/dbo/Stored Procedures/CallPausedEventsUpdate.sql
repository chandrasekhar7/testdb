CREATE PROCEDURE [dbo].[CallPausedEventsUpdate] (
	 @CallSid CHAR(34)
	,@EndTime DATETIME
)
AS
	BEGIN
	SET NOCOUNT ON
/****************************************************************************************************
--------------Change History----------------------section START--------------------------------------
*****************************************************************************************************
	DEVELOPER NAME		DATE				DESC

*****************************************************************************************************
--------------Change History----------------------section END----------------------------------------
*****************************************************************************************************/

	UPDATE TOP (1) CallPausedEvents
        SET EndTime = @EndTime
    WHERE CallSid = @CallSid AND EndTime IS NULL

END